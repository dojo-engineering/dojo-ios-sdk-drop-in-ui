import UIKit
import dojo_ios_sdk

struct FieldUtils {
    static func isEmailValid(_ email: String) -> Bool {
        let emailRegEx = "[A-Z0-9a-z._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,64}"
        return CommonUtils.evaluateRegex(regex: emailRegEx, textToVerify: email)
    }
}
